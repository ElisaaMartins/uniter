# expressões booleanas
# 2 + 2 < 4
if (2 + 2 < 4):
    print('Verdadeiro')

# 7 // 3 = 1 + 1
if (7 // 3 == 1 + 1):
    print('Verdadeiro')

# 3^2 + 4^2 = 25
if (3**2 + 4**2 == 25):
    print('Verdadeiro')

# 2 + 4 + 6 > 12
if (2 + 4 + 6 > 12):
    print('Verdadeiro')

# 1387 divisivel por 19
if (1387 % 19 == 0):
    print('Verdadeiro')

# 31 é par
if (31 % 2 == 0):
    print('Verdadeiro')

# menor valor entre: 34, 29 31 que é menor que 30
if (min(34, 29, 31) < 30):
    print('Verdadeiro')